/**Estradas ou trilhos
Neste desafio, você usará um operador ternário para decidir se tomará um carro ou trem baseado no menor tempo de viagem. O operador ternário usa um ponto de interrogação ? E dois pontos : para substituir uma if...else statement inteira por uma única linha de código. Por exemplo: test ? doThisIfTrue : elseDoThis.

INSTRUÇÕES
Na parte inferior do código:

Adicione um operador ternário que testa se o valor de car é menor que o valor de train
Imprimir 'Dirigir um carro' se o teste for true
Imprimir 'Comprar passagens de trem' se o teste for false */

var timeOffWork = 7;
var daysNeeded = 5;
timeOffWork > daysNeeded ? print('Go on vacation') : print('Go to work');
var train = 3;
var car = 2;
car < train ? print('Drive a car') : print('Buy train tickets');
/**Laço de strings
Neste desafio, será usado um laço for...of para imprimir cada letra de uma string, linha por linha. O Laço for...of permite que você repita o mesmo bloco de código várias vezes. Por exemplo, for (var element of 'hello') {} executaria o código dentro das {} do bloco 5 vezes, pois a string 'hello' tem 5 caracteres.

INSTRUÇÕES
Imprima cada letra da string 'grasshopper' em sua própria linha.

Adicione uma função print() dentro das {} do bloco do laço for...of
Dentro da função print(), insira a variável de laço letter */

for (var letter of 'grasshopper') {
    print(letter);
  }